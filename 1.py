# Вычислить число c заданной точностью d

# Пример:

# - при $d = 0.001, π = 3.141.$ 

# π = (4/1) - (4/3) + (4/5) - (4/7) + (4/9) - (4/11) + (4/13) - (4/15) ...

d = int(input('Enter accuracy  = '))
i = 1
pi = 0
while i < 10000000:
    pi = 4 / i - 4 / (i + 2) + pi
    i += 4
print (pi)
print (f'pi = {round(pi, d)}')